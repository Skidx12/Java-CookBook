public class relationalOperator {
    public static void main(String[] args) {
        // Relation operators are mostly used to compare the values
        
        int x = 6;
        int y = 5;

        // Left to right comparison
        boolean result = x<y; //less than operator
        boolean result1 = x>y; //more than operator
        boolean result2 = x<=y; //less than or equal operator
        boolean result3 = x>=y; //more than or equal operator
        boolean result4 = x==y; //check equals
        
        System.out.println(result);
        System.out.println(result1);
        System.out.println(result2);
        System.out.println(result3);
        System.out.println(result4);

    }
}
